    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
        String direccion,id; double vt,m2c,m2t; int ct,ant,piso;
        if (Diret.getText().isEmpty() || CantAmbT.getText().isEmpty() || Vtt.getText().isEmpty() || M2tt.getText().isEmpty() || M2Ct.getText().isEmpty()){
            Ventana_Mensaje("Hay campos sin completar!","Hay campos vacios");
            return;
        }
        if (Casa.isSelected()){
            if (Antigu.getText().isEmpty()){
                Ventana_Mensaje("Hay campos sin completar!","Hay campos vacios");
                return;
            }
         try{
             direccion=Diret.getText();
             vt=Double.parseDouble(Vtt.getText());
             ct=Integer.parseInt(CantAmbT.getText());
             m2c=Double.parseDouble(M2Ct.getText());
             m2t=Double.parseDouble(M2tt.getText());
             ant=Integer.parseInt(Antigu.getText());
             
         }catch(NumberFormatException e){
             Ventana_Mensaje("Hay campos invalidos!!","Error, Campos Invalidos");
             return;}
         if (m2c > m2t){
             Ventana_Mensaje("Los metros Cubiertos no pueden ser mayores a los totales","Metros Cubiertos > Totales");
             return;
         }
         Casa c1=new Casa(ant,Per,h,direccion,vt,ct,m2c,m2t);
         aux=Propiedades.buscarPropiedadPorDireccion(direccion);
         if (aux!=null){
            Ventana_Mensaje("Ya existe una propiedad con la direccion dada","Direccion ya cargada");
             return;
         }
         else{
          Propiedades.add(c1);
          Ventana_Mensaje("Se ha cargado la propiedad exitosamente!","Propiedad cargada!");
           MostrarTodos m=new MostrarTodos(Propiedades,this.Principal);
            this.setVisible(false);
            try {
                this.finalize();
            }catch (Throwable ex){}
            this.Principal.setSize(745,360);
            this.Principal.setContentPane(m);
         }
        }
         if (Depa.isSelected()){
            if (pisot.getText().isEmpty() || idt.getText().isEmpty()){
                Ventana_Mensaje("Hay campos sin completar!","Hay campos vacios");
                return;
            }
            try{
             direccion=Diret.getText();
             vt=Double.parseDouble(Vtt.getText());
             ct=Integer.parseInt(CantAmbT.getText());
             m2c=Double.parseDouble(M2Ct.getText());
             m2t=Double.parseDouble(M2tt.getText());
             piso=Integer.parseInt(pisot.getText());
             id=idt.getText();
         }catch(NumberFormatException e){
             Ventana_Mensaje("Hay campos invalidos!!","Error, Campos Invalidos");
             return;}
            if (m2c > m2t){
             Ventana_Mensaje("Los metros Cubiertos no pueden ser mayores a los totales","Metros Cubiertos > Totales");
             return;}
           Departamento d1=new Departamento(piso,id,Per,h,direccion,vt,ct,m2c,m2t);
           aux=Propiedades.buscarPropiedadPorDireccion(direccion);
         if (aux!=null){
             Ventana_Mensaje("Ya existe una propiedad con la direccion dada","Direccion ya cargada");
             return;
         }
         else{
          Propiedades.add(d1);
          Ventana_Mensaje("Se ha cargado la propiedad exitosamente!","Propiedad cargada!");
           MostrarTodos m=new MostrarTodos(Propiedades,this.Principal);
            this.setVisible(false);
            try {
                this.finalize();
            }catch (Throwable ex){}
            this.Principal.setSize(745,360);
            this.Principal.setContentPane(m);
         }
        }
    }//GEN-LAST:event_AceptarActionPerformed
